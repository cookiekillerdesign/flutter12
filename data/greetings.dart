// Pick `count` greetings, preferring gender match.
List<String> greetingsFor(String name, int age, {int count = 8}) {
  final g = detectGender(name);
  var matches = _db.where((t) => t.gender == g || t.gender == 'n').toList();
  if (matches.length < count) matches = List.of(_db);
  matches.shuffle(Random());
  final picked = matches.take(count).toList();
  final nm = name.trim().isEmpty ? 'друг' : name.trim();
  return picked.map((t) => t.fn(nm, age)).toList();
}
