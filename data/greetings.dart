// Detect gender from Russian name. -а/-я/-ия/-ья => female, else male/neutral.
String detectGender(String name) {
  final n = name.trim();
  if (n.isEmpty) return 'n';
  final e = n.substring(n.length - 1);
  final e2 = n.length >= 2 ? n.substring(n.length - 2) : '';
  if (e == 'а' || e == 'я') return 'f';
  if (e2 == 'ия' || e2 == 'ья') return 'f';
  return 'm';
}

